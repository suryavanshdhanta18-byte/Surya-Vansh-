import numpy as np

print(" Smart Calculator ")
print("1. Addition")
print("2. Subtraction")
print("3. Multiplication")
print("4. Division")
print("5. Power Calculation")
print("6. Modulus Operation")

choice = int(input("Enter your choice (1-6): "))

n = int(input("How many numbers do you want to enter? "))

nums = []
for i in range(n):
    nums.append(float(input(f"Enter number {i+1}: ")))

# storing in NumPy array
arr = np.array(nums)

# Operations using looping method
if choice == 1:  # Addition
    result = 0
    for num in arr:
        result += num
    print("Result:", result)

elif choice == 2:  # Subtraction
    result = arr[0]
    for i in range(1, len(arr)):
        result -= arr[i]
    print("Result:", result)

elif choice == 3:  # Multiplication
    result = 1
    for num in arr:
        result *= num
    print("Result:", result)

elif choice == 4:  # Division
    result = arr[0]
    for i in range(1, len(arr)):
        if arr[i] == 0:
            print("Cannot divide by zero!")
            break
        result /= arr[i]
    else:
        print("Result:", result)

elif choice == 5:  # Power
    result = arr[0]
    for i in range(1, len(arr)):
        result = result ** arr[i]
    print("Result:", result)

elif choice == 6:  # Modulus
    result = arr[0]
    for i in range(1, len(arr)):
        if arr[i] == 0:
            print("Cannot do modulus by zero!")
            break
        result %= arr[i]
    else:
        print("Result:", result)

else:
    print("Invalid choice! Please choose between 1 to 6.")

