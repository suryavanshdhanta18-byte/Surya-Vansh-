#Student Marks Analyzer
import numpy as np

n = int(input("Enter the number of students: "))

marks_list = []  # empty list to store the marks
grades = [] # empty list to store the grades

for i in range(n):
    marks = int(input(f"Enter marks of student {i+1}: "))
    marks_list.append(marks)

    while marks < 0 or marks > 100:
        print("Invalid marks! Enter between 0 and 100.")
        marks = int(input(f"Enter marks of student {i+1}: "))


    # Calculating grades and adding them to the list for the current student
    if marks >= 90:
        grades.append("A")
    elif marks >= 75:
        grades.append("B")
    elif marks >= 50:
        grades.append("C")
    else:
        grades.append("Fail")

print()   # To give a line space in the output
for i in range(n):    # Printing the grades along with the marks
 print(f"Student {i+1} - Marks: {marks_list[i]}, Grade: {grades[i]}")
arr = np.array(marks_list)   # Array of marks - Use marks_list


print()
